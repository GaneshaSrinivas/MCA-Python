from tkinter import *
def display():
    str1=e1.get()
    str2=e2.get()
    str3=int(e1.get())+int(e2.get())
    str3=str(str3)
    e3.delete(0,END)
    e3.insert(0,str3)

root=Tk()
root.title("Addition")
f = Frame(root,height=350,width=500)
f.propagate(0)
f.pack()
e1=IntVar()
e2=IntVar()
e3=IntVar()
l1=Label(text='Enter first number:')
l2=Label(text='Enter second number:')
b=Button(f,text='Add',command=display)
l3=Label(text='Sum')
e1=Entry(f,width=25,fg='white',bg='blue')
e2=Entry(f,width=25,fg='white',bg='blue')
e3=Entry(f,width=25,fg='white',bg='blue')
e3.pack()
l1.place(x=50,y=100)
e1.place(x=200,y=100)
l2.place(x=50,y=150)
l3.place(x=50,y=300)
e2.place(x=200,y=150)
e3.place(x=200,y=300)
b.place(x=250,y=200)
root.mainloop()
